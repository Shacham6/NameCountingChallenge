public class StringPair {

    String left, right;

    public StringPair(String name, String nickname) {
        this.left = name;
        this.right = nickname;
    }

    public String getLeft() {
        return left;
    }

    public void setLeft(String left) {
        this.left = left;
    }

    public String getRight() {
        return right;
    }

    public void setRight(String right) {
        this.right = right;
    }
}
